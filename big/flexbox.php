The Flexible Box Module, mostly referred to as flexbox is a layout model that allows elements to have powerful alignment capabilities and  distribute space within a container. “Using flexible widths and heights, elements can be aligned to fill a space or distribute space between elements, which makes it a great tool to use for responsive design systems.“ Using the flexbox method can be very beneficial to your website, depending what you’re striving for in the appearance. With flexbox being widely supported by all modern browsers, you’re able to create a beautiful responsive layout to your users. Some of the older browsers still have some problems with flexbox so keep in mind you will still need something to support the older browsers. According to Anselm Hannemann, going to the extent of researching the user’s browser share is very important. Determining the right browser support early on is recommended so you can target the testing range of your website. Flexbox can be incorporated with your layout to create a responsive design. 
